/*3. Write a program to pass an integer parameter to the application and print its square and cube
*/

import java.util.*;

class Q3{
	public static void main(String args[]){
	int i, x=0;
	for(i=0; i< args[0].length(); i++){
	if(!Character.isDigit(args[0].charAt(i))){
	x++;
	}
	}
	if(x==0){
	int n1 = Integer.parseInt(args[0]);
	System.out.println("Square: "+(n1*n1)+"; Cube: "+(n1*n1*n1));
	}
	else{
	System.out.println("Parameter not integer");
	}
	}
};
