/*Write a CPP program, that takes 2 integer numbers as input from user, calculates sum and difference , and prints the results.*/

#include<iostream>

using namespace std;

int main(){
	int num1, num2, sum, diff;
	cout<<"Enter two numbers"<<endl;
	cin>>num1>>num2;
	sum=num1+num2;
	diff=num1-num2;
	cout<<"The sum of "<<num1<<" and "<<num2<<" is: "<<sum<<endl;
	cout<<"The difference of "<<num1<<" and "<<num2<<" is: "<<diff<<endl;
	
	return(0);

}
